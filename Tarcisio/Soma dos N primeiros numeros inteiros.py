n = 1
soma = 0

print("Programa para cálcular a soma dos N primeiros números")
print("============================================================")
print("Obs.: Digite '0' para Sair!")
print("============================================================")
    
while n != 0:
    
    n = int(input("Digite um número: "))
    soma = soma + n
    print("Soma: ", soma)
    
print("Fim da execução do programa!")
print("=============================================")
print("Soma final: ", soma)
    
    